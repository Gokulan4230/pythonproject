def add(number1, number2):
    return number1 + number2


def sub(number1, number2):
    return number1 - number2


def square(number1, number2):
    return number1 * number2


def div(number1, number2):
    return number1 % number2


class question:
    def __init__(self, question, answer):
        self.question = question
        self.answer = answer